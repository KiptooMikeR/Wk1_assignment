// Task 1: Define Variables.
void defineVariables() {
  // Integer
  int age = 25;
  print('Integer: $age');
  
  // Double
  double height = 5.7;
  print('Double: $height');
  
  // String
  String name = 'Alice';
  print('String: $name');
  
  // Boolean
  bool isStudent = true;
  print('Boolean: $isStudent');
  
  // List of integers
  List<int> numbers = [1, 2, 3, 4, 5];
  print('List<int>: $numbers');
}